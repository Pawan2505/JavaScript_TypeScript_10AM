// ===============================
// JavaScript Variables
// ===============================

// Variable:
// A variable is like a container where we can store data.

// -------------------------------
// Rules for Variable Naming
// -------------------------------

// Rule 1: Variable name cannot start with a number

// 12num;          
// var 12num;      

var num12;         

// -------------------------------
// Rule 2: No white space in variable name

// var first number;   

var first_number;     
var firstNumber;      

// -------------------------------
// Rule 3: JavaScript is case-sensitive

let num = 10;
let Num = 20;

// num and Num are different variables

// -------------------------------
// Rule 4: Only _ and $ are allowed special characters

var _num1;            
var $num2;            

// var @num3;         

// -------------------------------
// Rule 5: Variable name should be meaningful

let x = 5000;         
let salary = 5000;   
